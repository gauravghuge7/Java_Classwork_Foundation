public class demoRelational {

   

   // () / * - +
   

   public static void main(String[] args) {
      

      // less than 

      int a = 20;
      int b = 25;


      System.out.println(a > b);  // false 

      System.out.println(a < b);  // true


      System.out.println(a == b);  // false

      System.out.println(a != b);  // true

      System.out.println(a >= b);  // false

      System.out.println(b >= a);  // true

   }

}